const API = 'http://localhost:8080';

async function apiFetch(url, options = {}) {
  const token = getToken();
  const headers = {
    'Content-Type': 'application/json',
    ...(options.headers || {})
  };
  if (token) {
    headers['Authorization'] = 'Bearer ' + token;
  }
  const res = await fetch(API + url, {
    ...options,
    headers
  });
  if (res.status === 401) {
    clearAuth();
    window.location.href = 'login.html';
    return;
  }
  const text = await res.text();
  const trimmed = text.trim();
  if (!trimmed) {
    if (!res.ok) {
      return { error: res.statusText || 'Request failed', status: res.status };
    }
    return null;
  }
  try {
    return JSON.parse(trimmed);
  } catch (e) {
    return { error: 'Invalid response from server', status: res.status, body: trimmed.slice(0, 200) };
  }
}

const APEX_FALLBACK_IMG = 'images/hero-apex-coming-soon.jpg';

function escapeHtml(str) {
  if (str == null) return '';
  return String(str)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/"/g, '&quot;');
}

/** Public catalog fetch — never sends JWT (avoids 401 redirect on marketing pages). */
async function apiFetchPublic(url, options = {}) {
  const headers = {
    'Content-Type': 'application/json',
    ...(options.headers || {})
  };
  const res = await fetch(API + url, {
    ...options,
    headers
  });
  const text = await res.text();
  const trimmed = text.trim();
  if (!trimmed) {
    if (!res.ok) {
      return { error: res.statusText || 'Request failed', status: res.status };
    }
    return null;
  }
  try {
    return JSON.parse(trimmed);
  } catch (e) {
    return { error: 'Invalid response from server', status: res.status };
  }
}

function classBadgeClass(classType) {
  const t = (classType || '').toUpperCase();
  if (t.includes('HIIT')) return 'hiit';
  if (t.includes('YOGA')) return 'yoga';
  if (t.includes('SPIN')) return 'spinning';
  return 'strength';
}

function formatClassWhen(iso) {
  if (!iso) return '';
  try {
    const d = new Date(iso);
    return d.toLocaleString(undefined, { dateStyle: 'medium', timeStyle: 'short' });
  } catch (e) {
    return iso;
  }
}

