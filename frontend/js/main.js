function initSmoothScroll() {
  document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
      const href = this.getAttribute('href');
      
      
      if (href === '#') {
        e.preventDefault();
        return;
      }
      
      const target = document.querySelector(href);
      if (target) {
        e.preventDefault();
        const headerHeight = 80;
        const targetPosition = target.offsetTop - headerHeight;
        
        window.scrollTo({
          top: targetPosition,
          behavior: 'smooth'
        });
      }
    });
  });
}




function animateCounter(element, end) {
  let start = 0;
  const duration = 2000;
  const frameRate = 60;
  const totalFrames = (duration / 1000) * frameRate;
  const increment = end / totalFrames;
  let currentFrame = 0;
  
  const timer = setInterval(() => {
    currentFrame++;
    start += increment;
    
    if (currentFrame >= totalFrames) {
      element.textContent = Math.floor(end) + '+';
      clearInterval(timer);
    } else {
      element.textContent = Math.floor(start);
    }
  }, 1000 / frameRate);
}




function initScrollReveal() {
  const observerOptions = {
    threshold: 0.1,
    rootMargin: '-50px'
  };
  
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('is-visible');
        
        
        if (entry.target.classList.contains('stat-number')) {
          const end = parseInt(entry.target.getAttribute('data-count'));
          animateCounter(entry.target, end);
        }
        
        observer.unobserve(entry.target);
      }
    });
  }, observerOptions);
  
  
  const selectors = ['.visible-on-scroll', '.reveal-left', '.reveal-right', '.stat-number'];
  selectors.forEach(selector => {
    document.querySelectorAll(selector).forEach(el => {
      observer.observe(el);
    });
  });
}




